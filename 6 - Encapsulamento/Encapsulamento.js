/*Encapsulamento é um dos principios de impedir que determinadas partes de uma classe seja acessadas
pela parte de fora, ou seja, quando encapsulamentos uma parte da classe, ela so pode ser acessada 
dentro da propria classe. Basicamente, é uma forma de proteger aquela determinada parte da classe

Temos dois niveis de encapsulamente: 
Publico: O nivel padrão, que pode ser acessado fora da classe
Privado: Onde o atributo/metodo so pode ser acessado dentro da classe

Para definirmos um atributo ou metodo privado, utilizamos # no começo do nome dele*/

class obj {
    #atributoPrivado 
}

/*Na maneira acima, ja definimos o encapsulamento no atributoprivado*/

